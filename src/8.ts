// A simple function that returns the sum of two numbers
function add(a: number, b: number): number {
  return a + b;
}

// A function that takes an array as input and returns the largest element in the array
function max(arr: Array<number>): number {
  return Math.max(...arr);
}

// A function that takes a string as input and returns the length of the string
function len(str: string): number {
  return str.length;
}
